//foreach loop html tag attribute display
function LOOP_TagsAttributes($data)
{
  foreach ($data as $key => $values) {
    echo "$key='$values'";
  }
}
function generateHtmlTag($tagName, $value, $attributes = array())
{
  // Initialize an empty string to store the attributes
  $attribute_string = '';

  // Check if the attributes array is not empty
  if (!empty($attributes)) {
    // Loop through the attributes array and concatenate them
    foreach ($attributes as $key => $val) {
      $attribute_string .= " $key='$val'";
    }
  }

  // Create and return the HTML tag with attributes
  return "<$tagName $attribute_string>$value</$tagName>";
}
